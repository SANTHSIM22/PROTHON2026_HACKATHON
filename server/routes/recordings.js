// Upload Endpoint
router.post('/upload', authMiddleware, upload.single('audioFile'), async (req, res) => {
  try {
    if (!req.file) {
      return res.status(400).json({ message: 'No file uploaded' });
    }

    console.log("Processing audio with deeply enhanced FFmpeg AI...");
    
    // Parse the incoming free transcript timestamps generated by Google Chrome native ML
    let transcriptObj = [];
    if (req.body.transcriptData) {
      try {
        transcriptObj = JSON.parse(req.body.transcriptData);
      } catch (e) {
        console.error("Failed to parse transcript", e);
      }
    }

    // Create a stream from the uploaded raw audio buffer
    const inputStream = streamifier.createReadStream(req.file.buffer);
    const passThrough = new PassThrough();
    const chunks = [];

    // Collect processed chunks
    passThrough.on('data', chunk => chunks.push(chunk));

    // When processing is totally finished, save to DB
    passThrough.on('end', async () => {
      const enhancedBuffer = Buffer.concat(chunks);

      const newRecording = new AudioRecording({
        userId: req.user.id,
        filename: `recording-${Date.now()}.mp3`, // Changed to mp3 for universal high quality compatibility
        audioData: enhancedBuffer,
        contentType: 'audio/mp3',
        transcript: transcriptObj // Save timestamps and text to the DB natively
      });

      await newRecording.save();
      console.log('Audio processed and perfectly saved to MongoDB!');

      res.status(201).json({
        message: 'File uploaded and enhanced successfully',
        recording: {
          _id: newRecording._id,
          filename: newRecording.filename,
          createdAt: newRecording.createdAt
        }
      });

      // Automatically trigger Deep Diarization in the background!
      (async () => {
        try {
          console.log(`[Auto-Mation] Starting background transcription for recording: ${newRecording._id}`);
          const transcriptInfo = await aiClient.transcripts.transcribe({
             audio: enhancedBuffer, 
             speaker_labels: true,
             speech_models: ["universal-3-pro"]
          });

          if (transcriptInfo.status !== 'error') {
            const SILENCE_THRESHOLD = 1500; 
            const finalBlocks = [];
            let currentBlock = null;

            if (transcriptInfo.words && transcriptInfo.words.length > 0) {
              transcriptInfo.words.forEach(word => {
                if (!currentBlock) {
                  currentBlock = { text: word.text, timestamp: word.start, endTime: word.end, speaker: word.speaker };
                } else {
                  if (word.speaker === currentBlock.speaker && (word.start - currentBlock.endTime) <= SILENCE_THRESHOLD) {
                    currentBlock.text += " " + word.text;
                    currentBlock.endTime = word.end;
                  } else {
                    finalBlocks.push(currentBlock);
                    currentBlock = { text: word.text, timestamp: word.start, endTime: word.end, speaker: word.speaker };
                  }
                }
              });
              if (currentBlock) finalBlocks.push(currentBlock);
            } else {
              finalBlocks.push(...(transcriptInfo.utterances || []).map(u => ({
                text: u.text, timestamp: u.start, endTime: u.end, speaker: u.speaker
              })));
            }

            await AudioRecording.findByIdAndUpdate(newRecording._id, { transcript: finalBlocks });
            console.log(`[Auto-Mation] Background transcription fully finished for recording: ${newRecording._id}`);
          }
        } catch (err) {
          console.error('[Auto-Mation] Background transcription failed:', err);
        }
      })();
    });

    // FFmpeg Processing Pipeline
    ffmpeg(inputStream)
      .audioCodec('libmp3lame')
      .audioBitrate('192k')     // Heavy encoding bitrate for deep clarity
      .audioChannels(1)         // Force Mono (centers the voice track natively)
      .audioFilters([
        'volume=5.0',           // MASSIVE Volume Boost right at the start to ensure low voices are caught!
        'highpass=f=80',        // EQ: Drops heavy AC rumbling, car noise, desk bumps
        'lowpass=f=8000',       // EQ: Cuts white noise/hissing artifacts above 8kHz
        'afftdn=nf=-20',        // AI: Mild noise reduction so we don't accidentally cut out quiet voices
        'loudnorm=I=-12:TP=-1.0:LRA=7' // EBU R128 Broadcast loudnorm pushed heavily to podcast standard levels
      ])
      .format('mp3')
      .on('error', (err) => {
        console.error('FFmpeg Audio Processing Error:', err);
        if (!res.headersSent) {
          res.status(500).json({ message: 'Error running FFmpeg audio enhancement framework' });
        }
      })
      .pipe(passThrough);

  } catch (err) {
    console.error('Upload Error:', err);
    res.status(500).json({ message: 'Server error during upload' });
  }
});

// Post-Processing: Run AI Diarization
router.post('/:id/transcribe', authMiddleware, async (req, res) => {
  try {
    const recording = await AudioRecording.findOne({ _id: req.params.id, userId: req.user.id });
    if (!recording) return res.status(404).json({ error: 'Recording not found' });
    
    // Convert DB buffer into format AssemblyAI accepts (requires handling Buffer properly or URL)
    // AssemblyAI handles streams/buffers natively in newer SDK
    const transcriptInfo = await aiClient.transcripts.transcribe({
       audio: recording.audioData, 
       speaker_labels: true, // True speaker separation (Diarization)
       speech_models: ["universal-3-pro"] // Updated AssemblyAI API requires this exactly as an array map!
    });
    
    if (transcriptInfo.status === 'error') {
       throw new Error(transcriptInfo.error);
    }
    
    // Rebuild utterances manually from word-level precision!
    // We split a speaker's text block if there is a silence of > 1500 milliseconds
    const SILENCE_THRESHOLD = 1500; 
    const finalBlocks = [];
    let currentBlock = null;

    if (transcriptInfo.words && transcriptInfo.words.length > 0) {
      transcriptInfo.words.forEach(word => {
        if (!currentBlock) {
          currentBlock = { 
            text: word.text, 
            timestamp: word.start, 
            endTime: word.end, 
            speaker: word.speaker 
          };
        } else {
          // If the same speaker is still talking AND the silence gap is less than our threshold
          if (word.speaker === currentBlock.speaker && (word.start - currentBlock.endTime) <= SILENCE_THRESHOLD) {
            currentBlock.text += " " + word.text; // Append voice
            currentBlock.endTime = word.end;      // Extend time
          } else {
            // A long silence happened OR the speaker changed! Save this chunk and start a new one.
            finalBlocks.push(currentBlock);
            currentBlock = { 
              text: word.text, 
              timestamp: word.start, 
              endTime: word.end, 
              speaker: word.speaker 
            };
          }
        }
      });
      // Don't forget to push the very last block
      if (currentBlock) {
        finalBlocks.push(currentBlock);
      }
    } else {
      // Fallback to AssemblyAI's default utterances if words aren't available for some reason
      finalBlocks.push(...(transcriptInfo.utterances || []).map(u => ({
        text: u.text,
        timestamp: u.start,
        endTime: u.end,
        speaker: u.speaker
      })));
    }

    recording.transcript = finalBlocks;
    await recording.save();

    res.json({ message: 'Diarization successful', transcript: finalBlocks });
  } catch (err) {
    console.error('Diarization Error:', err);
    res.status(500).json({ error: 'AI Transcription failed. Make sure valid AssemblyAI API key is set.' });
  }
});

