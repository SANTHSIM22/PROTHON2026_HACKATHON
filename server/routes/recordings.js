// Organization: Get merged transcripts for a specific meeting name
router.get('/organization/meetings/:meetingName', authMiddleware, async (req, res) => {
  try {
    const { meetingName } = req.params;
    
    // Find all recordings (from ANY user) assigned to this meeting
    const recordings = await AudioRecording.find({ meetingName })
                                           .populate('userId', 'name')
                                           .select('-audioData');
    
    // We want to combine their transcripts dynamically and sort chronologically
    let combinedTranscript = [];

    recordings.forEach(rec => {
      if (rec.transcript && rec.transcript.length > 0) {
        const recordingStartTime = new Date(rec.createdAt).getTime();
        rec.transcript.forEach(t => {
          combinedTranscript.push({
            recordingId: rec._id,
            userName: rec.userId.name, // THE ACCOUNT NAME of whoever recorded it
            userId: rec.userId._id,
            text: t.text,
            timestamp: t.timestamp,
            absoluteTime: recordingStartTime + t.timestamp, // To sort across different files!
            endTime: t.endTime,
            aiSpeakerLabel: t.speaker // their local diarization label if any
          });
        });
      }
    });

    // Sort the master timeline chronologically across all users using true absolute times
    combinedTranscript.sort((a, b) => a.absoluteTime - b.absoluteTime);

    res.json({
      meetingName,
      participants: [...new Set(recordings.map(r => r.userId.name))],
      transcript: combinedTranscript,
      needsTranscription: recordings.some(rec => !rec.transcript || rec.transcript.length === 0)
    });

  } catch (err) {
    console.error('Org Meeting Error:', err);
    res.status(500).json({ error: 'Failed to fetch meeting transcripts' });
  }
});

